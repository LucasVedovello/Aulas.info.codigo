package main.java.com.sgl.model;

public class Usuario {
    private String nome;
    private int IdUsuario;
    
    public Usuario(String nome, int IdUsuario){
        this.nome = nome;
        this.IdUsuario = IdUsuario;

    }

    public String getNome(){
        return nome;
    }

    public int getIdUsuario(){
        return IdUsuario;
    }

    












}
